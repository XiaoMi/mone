package com.xiaomi.youpin.gwdash.filter;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import javax.servlet.*;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;
import java.util.HashMap;
import java.util.Map;

public class OptRecordFilter implements Filter {

    private static final Logger LOGGER = LoggerFactory.getLogger(OptRecordFilter.class);

    @Override
    public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain) throws IOException, ServletException {
        HttpServletRequest httpRequest = (HttpServletRequest) request;
        HttpServletResponse httpResponse = (HttpServletResponse) response;

        String url = httpRequest.getRequestURL().toString();
        Map<String, String[]> params = httpRequest.getParameterMap();
        params = new HashMap<>(params);
        chain.doFilter(request, response);

        int status = httpResponse.getStatus();

        LOGGER.info("[OptRecordFilter.doFilter] opt logï¼Œ url: {}, params: {}, response status: {}",
                url, params, status);

    }
}
