package com.xiaomi.mone.app.controller;

import com.xiaomi.mone.app.service.HeraJobAppService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

import static com.xiaomi.mone.app.common.Constant.SUCCESS_MESSAGE;

/**
 * @author wtt
 * @version 1.0
 * @description
 * @date 2022/10/31 16:36
 */
@RestController
public class AppToolController {

    @Autowired
    private HeraJobAppService heraJobAppService;

    @GetMapping("tool/mis/app/name")
    public String queryById(@RequestParam("name") String name) {
        heraJobAppService.synchronousMisApp(name);
        return SUCCESS_MESSAGE;
    }
}
